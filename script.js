/**************** COLLECTIONS SLIDER LOGIC ****************/
const track = document.getElementById("collectionsTrack");
const btnPrev = document.getElementById("collectionsPrev");
const btnNext = document.getElementById("collectionsNext");

function getScrollAmount() {
  const card = track.querySelector(".collection-card");
  if (!card) return 300; // fallback
  const cardWidth = card.offsetWidth;
  const gap = 18;
  return cardWidth + gap;
}

btnNext.addEventListener("click", () => {
  track.scrollBy({ left: getScrollAmount(), behavior: "smooth" });
});

btnPrev.addEventListener("click", () => {
  track.scrollBy({ left: -getScrollAmount(), behavior: "smooth" });
});



/**************** RENDER RESTAURANTS FUNCTION ****************/
function renderRestaurants(list) {
  const root = document.getElementById("root");

  // clear existing restaurants
  root.replaceChildren();

  list.forEach(restraunt => {
    const card = document.createElement("div");
    card.classList.add("card");

    const image = document.createElement("img");
    image.src = `Images/${restraunt.images}.jpeg`; // ensure folder/images exist

    const cardContent = document.createElement("div");
    cardContent.classList.add("card-content");

    // Header
    const cardHeader = document.createElement("div");
    cardHeader.classList.add("card-header");

    const name = document.createElement("h3");
    name.textContent = restraunt.Names;

    const rating = document.createElement("span");
    rating.classList.add("rating");
    rating.textContent = "Rating " + restraunt.rating;

    cardHeader.appendChild(name);
    cardHeader.appendChild(rating);

    // Footer price + type
    const cardFooter = document.createElement("div");
    cardFooter.classList.add("card-footer");

    const foodType = document.createElement("span");
    foodType.textContent = restraunt.food_type;

    const price = document.createElement("span");
    price.textContent = "₹" + restraunt.Price_for_two;

    cardFooter.appendChild(foodType);
    cardFooter.appendChild(price);

    // Location + distance
    const cardLocation = document.createElement("div");
    cardLocation.classList.add("card-location");

    const location = document.createElement("span");
    location.textContent = restraunt.location;

    const distance = document.createElement("span");
    distance.textContent = restraunt.distance_from_customer_house + " km";

    cardLocation.appendChild(location);
    cardLocation.appendChild(distance);

    // Append content to card
    cardContent.appendChild(cardHeader);
    cardContent.appendChild(cardFooter);
    cardContent.appendChild(cardLocation);

    card.appendChild(image);
    card.appendChild(cardContent);
    root.appendChild(card);
  });
}

