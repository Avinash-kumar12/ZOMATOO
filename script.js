// apply filter
document.getElementById("applyFilter").addEventListener("click", () => {
  const selected = document.querySelector('input[name="filterOption"]:checked');

  if (!selected) {
    alert("Please select an option!");
    return;
  }

  const value = selected.value;
  const sorted = [...restraunts]; // prevent modifying original array

  if (value === "rating") {
    sorted.sort((a, b) => b.rating - a.rating);
  } else if (value === "highLow") {
    sorted.sort((a, b) => b.Price_for_two - a.Price_for_two);
  } else if (value === "costLowHigh") {
    sorted.sort((a, b) => a.Price_for_two - b.Price_for_two);
  } else if (value === "distance") {
    sorted.sort(
      (a, b) =>
        Number(a.distance_from_customer_house) -
        Number(b.distance_from_customer_house)
    );
  }

  renderRestaurants(sorted);
  filterPopup.classList.add("hidden");
});



